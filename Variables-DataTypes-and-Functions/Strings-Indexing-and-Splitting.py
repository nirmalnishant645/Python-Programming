'''
Print the positive and negative indices of the string and split the first two characters of the string and print.
'''

a = "Hi There!"
'''Indexing [0 to 8] or [-9 to -1]'''
print("Index 0",a[0])
print("Index 8",a[8])
print("Index -1",a[-1])
print("Index -9",a[-9])
'''Splitting [0 to 2] or [-9 to -7]'''
print("Splitting:",a[0:2])
print("Splitting:",a[:2])
print("Splitting:",a[-9:-7])
print("Splitting:",a[:-7])
